package class3;

public class DoubleComparison {
    public static void main(String[] args) {
        double checkingAccount=2000;
        double savingAccount=4000;
        if (checkingAccount>savingAccount) {
            System.out.println("Double value " + checkingAccount + " is larger than " + savingAccount);
        }else {
            System.out.println("Double value "+savingAccount+" is larger than "+checkingAccount);
        }
    }
}
