package class6;

public class E5SwitchStatment {
    public static void main(String[] args) {
     char number='z';//double,boolean,flat,long --works with string,char,byte and int
     switch (number){
         case 'F':
             System.out.println("Female");
             break;
         case 'M':
             System.out.println("Male");
             break;
         case 'O':
             System.out.println("Others");
             break;
         default:
             System.out.println("Char not supported");
     }


        }
      }



